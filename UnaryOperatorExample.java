public class UnaryOperatorExample{
public static void main(String args[]){
int number=+76;

System.out.println(number);
number=-number;
System.out.println(number);
int a=20;
a++;
System.out.println(a);
a--;
System.out.println(a);
boolean flag=false;
System.out.println(flag);
System.out.println(!flag);
}
}